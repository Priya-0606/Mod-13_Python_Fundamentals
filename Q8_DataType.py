# Demonstrating basic Python data types

inte = 42
print(type(inte))

Float = 3.14
print(type(Float))

Str = "Hello, Python!"
print(type(Str))

List = [1, 2, 3, 4]
print(type(List))

Tup= (5, 6, 7)
print(type(Tup))

Dict = {"name": "Alice", "age": 25}
print(type(Dict))

Set = {1, 2, 3}
print(type(Set))
